// 3. Generic Project Plan PDF
// ============================
export async function generateProjectPlanPDF(agendaData: Record<string, unknown>, projectName: string): Promise<Uint8Array> {
  const doc = createBasePDF();
  const W = doc.internal.pageSize.getWidth();
  let y = 45;
  let pageNum = 1;

  addHeader(doc, "Project Management Plan", `خطة إدارة المشروع — ${projectName}`, pageNum);
  addFooter(doc);

  // Overview
  y = addSectionTitle(doc, "1. Project Overview", y);
  doc.setFontSize(9); doc.setFont("helvetica", "normal"); doc.setTextColor(...COLORS.textMid as [number,number,number]);
  const overviewLines = doc.splitTextToSize((agendaData.project_overview as string) || "", W - 30);
  doc.text(overviewLines, 15, y);
  y += overviewLines.length * 5 + 5;

  // Methodology badge
  doc.setFillColor(...COLORS.primary as [number,number,number]);
  doc.roundedRect(15, y, 70, 10, 2, 2, "F");
  doc.setFontSize(8); doc.setFont("helvetica", "bold"); doc.setTextColor(255, 255, 255);
  doc.text(`Methodology: ${agendaData.methodology || "Predictive"}`, 50, y + 7, { align: "center" });

  doc.setFillColor(...COLORS.gold as [number,number,number]);
  doc.roundedRect(90, y, 70, 10, 2, 2, "F");
  doc.setTextColor(30, 30, 30);
  doc.text(`Est. Effort: ${agendaData.estimated_effort_hours || 0} hrs`, 125, y + 7, { align: "center" });
  y += 20;

  // Phases table
  y = addSectionTitle(doc, "2. Project Phases", y);
  const phases = (agendaData.phases as Array<{ name: string; start_week: number; end_week: number; description: string; deliverables: string[] }>) || [];
  autoTable(doc, {
    startY: y,
    head: [["Phase", "Start Wk", "End Wk", "Description", "Key Deliverables"]],
    body: phases.map(p => [
      p.name,
      `Wk ${p.start_week}`,
      `Wk ${p.end_week}`,
      p.description.substring(0, 50),
      (p.deliverables || []).slice(0, 2).join(", "),
    ]),
    styles: { font: "helvetica", fontSize: 8, cellPadding: 3 },
    headStyles: { fillColor: COLORS.navy as [number,number,number], textColor: 255, fontStyle: "bold" },
    alternateRowStyles: { fillColor: COLORS.bgLight as [number,number,number] },
    columnStyles: {
      0: { cellWidth: 30 }, 1: { cellWidth: 16, halign: "center" },
      2: { cellWidth: 16, halign: "center" }, 3: { cellWidth: 65 }, 4: { cellWidth: 50 },
    },
    margin: { left: 15, right: 15 },
  });
  y = (doc as any).lastAutoTable.finalY + 12;

  // New page for milestones & KPIs
  doc.addPage(); pageNum++;
  addHeader(doc, "Project Management Plan", `خطة إدارة المشروع — ${projectName}`, pageNum);
  addFooter(doc);
  y = 45;

  // Milestones
  y = addSectionTitle(doc, "3. Key Milestones", y);
  const milestones = (agendaData.key_milestones as Array<{ name: string; week: number; description: string; success_criteria: string }>) || [];
  autoTable(doc, {
    startY: y,
    head: [["Milestone", "Week", "Description", "Success Criteria"]],
    body: milestones.map(m => [m.name, `Wk ${m.week}`, m.description, m.success_criteria]),
    styles: { font: "helvetica", fontSize: 8, cellPadding: 3 },
    headStyles: { fillColor: COLORS.navy as [number,number,number], textColor: 255, fontStyle: "bold" },
    alternateRowStyles: { fillColor: COLORS.bgLight as [number,number,number] },
    columnStyles: { 0: { cellWidth: 40 }, 1: { cellWidth: 15, halign: "center" }, 2: { cellWidth: 60 }, 3: { cellWidth: 60 } },
    margin: { left: 15, right: 15 },
  });
  y = (doc as any).lastAutoTable.finalY + 12;

  // KPIs
  y = addSectionTitle(doc, "4. Key Performance Indicators (KPIs)", y);
  const kpis = (agendaData.kpis as Array<{ name: string; target: string; measurement_method: string; frequency: string }>) || [];
  autoTable(doc, {
    startY: y,
    head: [["KPI", "Target", "Measurement Method", "Frequency"]],
    body: kpis.map(k => [k.name, k.target, k.measurement_method, k.frequency]),
    styles: { font: "helvetica", fontSize: 8, cellPadding: 3 },
    headStyles: { fillColor: COLORS.success as [number,number,number], textColor: 255, fontStyle: "bold" },
    alternateRowStyles: { fillColor: [240, 253, 244] },
    margin: { left: 15, right: 15 },
  });

  return doc.output("arraybuffer") as unknown as Uint8Array;
}
