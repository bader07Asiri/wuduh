// 2. Risk Register PDF
// ============================
export async function generateRiskRegisterPDF(data: Record<string, unknown>, projectName: string): Promise<Uint8Array> {
  const doc = createBasePDF();
  const W = doc.internal.pageSize.getWidth();
  let y = 45;

  addHeader(doc, "Risk Register", `سجل المخاطر — ${projectName}`);
  addFooter(doc);

  // Summary boxes
  const summary = data.summary as { total_risks: number; critical: number; high: number; medium: number; low: number; overall_risk_level: string } || {};
  const summBoxes = [
    { label: "Total Risks", value: String(summary.total_risks || 0) },
    { label: "Critical", value: String(summary.critical || 0) },
    { label: "High", value: String(summary.high || 0) },
    { label: "Medium", value: String(summary.medium || 0) },
    { label: "Low", value: String(summary.low || 0) },
  ];
  const bw = (W - 30) / 5;
  summBoxes.forEach((b, i) => addInfoBox(doc, b.label, b.value, 15 + i * (bw + 1), y, bw - 1, 18));
  y += 28;

  // Risk table
  const risks = (data.risks as Array<{
    id: string; risk_statement: string; category: string;
    probability_score: number; impact_score: number; risk_score: number;
    risk_level: string; response_strategy: string; response_actions: string[];
    contingency_plan: string; owner: string; status: string;
  }>) || [];

  autoTable(doc, {
    startY: y,
    head: [["ID", "Risk Statement", "Cat.", "P", "I", "Score", "Level", "Strategy", "Owner"]],
    body: risks.map(r => [
      r.id,
      r.risk_statement.substring(0, 60) + (r.risk_statement.length > 60 ? "..." : ""),
      r.category,
      r.probability_score,
      r.impact_score,
      r.risk_score,
      r.risk_level,
      r.response_strategy,
      r.owner,
    ]),
    styles: { font: "helvetica", fontSize: 7, cellPadding: 2 },
    headStyles: { fillColor: COLORS.navy as [number,number,number], textColor: 255, fontStyle: "bold", fontSize: 7 },
    alternateRowStyles: { fillColor: COLORS.bgLight as [number,number,number] },
    columnStyles: {
      0: { cellWidth: 12 }, 1: { cellWidth: 55 }, 2: { cellWidth: 16 },
      3: { cellWidth: 8, halign: "center" }, 4: { cellWidth: 8, halign: "center" },
      5: { cellWidth: 12, halign: "center" }, 6: { cellWidth: 16 },
      7: { cellWidth: 18 }, 8: { cellWidth: 20 },
    },
    didParseCell: (hookData) => {
      if (hookData.column.index === 5 && hookData.section === "body") {
        const score = Number(hookData.cell.raw);
        if (score >= 15) hookData.cell.styles.fillColor = [254, 202, 202];
        else if (score >= 8) hookData.cell.styles.fillColor = [254, 215, 170];
        else if (score >= 4) hookData.cell.styles.fillColor = [254, 249, 195];
        else hookData.cell.styles.fillColor = [167, 243, 208];
      }
    },
    margin: { left: 15, right: 15 },
  });

  return doc.output("arraybuffer") as unknown as Uint8Array;
}

